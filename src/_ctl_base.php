    /**
     * Ajusta los elementos de salida de un alta bd
     * @param bool $header is header aplica redirect
     * @param stdClass $data_retorno datos de redirect para get
     * @param stdClass $result Resultado de la transaccion
     * @param bool $ws is ws muestra salida json
     * @return stdClass|void
     * @version 8.2.0
     */
    final protected function out_alta_bd(bool $header, stdClass $data_retorno, stdClass $result, bool $ws){
        if(!isset($data_retorno->siguiente_view)){
            $data_retorno->siguiente_view = '';
        }
        if($header){

            if($data_retorno->id_retorno === -1) {
                $data_retorno->id_retorno = $result->registro_id;
            }
            $this->retorno_base(
                registro_id:$data_retorno->id_retorno, result: $result,
                siguiente_view: $data_retorno->siguiente_view, ws:  $ws,seccion_retorno: $data_retorno->seccion_retorno);

        }
        if($ws){
            header('Content-Type: application/json');
            try {
                echo json_encode($result, JSON_THROW_ON_ERROR);
            }
            catch (Throwable $e){
                $error = (new errores())->error(mensaje: 'Error al maquetar JSON' , data: $e);
                print_r($error);
            }
            exit;
        }
        $result->siguiente_view = $data_retorno->siguiente_view;

        return $result;
    }

    protected function retorno(
        stdClass $data_retorno, bool $header, int $registro_id, mixed $result, bool $ws){
        if($header){
            if($data_retorno->id_retorno === -1) {
                $data_retorno->id_retorno = $registro_id;
            }

            $this->retorno_base(registro_id:$data_retorno->id_retorno, result: $result, siguiente_view: $data_retorno->siguiente_view,
                ws:  $ws,seccion_retorno: $data_retorno->seccion_retorno);

        }
        if($ws){
            header('Content-Type: application/json');
            try {
                echo json_encode($result, JSON_THROW_ON_ERROR);
            }
            catch (Throwable $e){
                $error = $this->errores->error(mensaje: 'Error al dar salida json', data: $e);
                print_r($error);
            }
            exit;
        }
        return $result;
    }

    /**
     * @param string $namespace_model
     * @param string $name_model_children
     * @param int $registro_id
     * @return array
     */
    private function rows_children(string $namespace_model, string $name_model_children, int $registro_id): array
    {
        $filtro = $this->genera_filtro_children(registro_id: $registro_id);
        if(errores::$error){
            return $this->errores->error(mensaje: 'Error al obtener filtro',data:  $filtro);
        }

        $model_children = $this->modelo->genera_modelo(modelo: $name_model_children,namespace_model: $namespace_model);
        if(errores::$error){
            return $this->errores->error(mensaje: 'Error al generar modelo',data:  $model_children);
        }

        $r_children = $model_children->filtro_and(filtro:$filtro);
        if(errores::$error){
            return $this->errores->error(mensaje: 'Error al obtener r_children',data:  $r_children);
        }
        return $r_children->registros;
    }

    /**
     * Obtiene la seccion de retorno
     * @return string
     * @version 0.282.38
     */
    private function seccion_retorno():string{
        $seccion_retorno = $this->tabla;
        if(isset($_POST['seccion_retorno'])){
            $seccion_retorno = $_POST['seccion_retorno'];
            unset($_POST['seccion_retorno']);
        }
        return $seccion_retorno;
    }

    /**
     * @param array $keys_selects
     * @return array|stdClass
     */
    final protected function upd_base_template(array $keys_selects): array|stdClass
    {
        $keys_selects = $this->key_select_base(keys_selects: $keys_selects);
        if(errores::$error){
            return $this->errores->error(mensaje: 'Error al integrar keys selects base',data:  $keys_selects);
        }

        $base = $this->base_upd(keys_selects: $keys_selects, params: array(),params_ajustados: array());
        if(errores::$error){
            return $this->errores->error(mensaje: 'Error al integrar base',data:  $base);
        }
        return $base;
    }

    /**
     * Valida la entrada de datos para childrens
     * @param string $namespace_model Namespace del paquete
     * @param string $name_model_children Nombre del modelo
     * @param int $registro_id Registro en proceso
     * @return bool|array
     * @version 4.33.1
     */
    private function valida_data_children(string $namespace_model, string $name_model_children,
                                          int $registro_id): bool|array
    {
        $namespace_model = trim($namespace_model);
        if($namespace_model === ''){
            return $this->errores->error(mensaje: 'Error namespace_model esta vacio',data:  $namespace_model);
        }

        $name_model_children = trim($name_model_children);
        if($name_model_children === ''){
            return $this->errores->error(mensaje: 'Error name_model_children esta vacio',data:  $name_model_children);
        }

        $this->tabla = trim($this->tabla);
        if($this->tabla === ''){
            return $this->errores->error(mensaje: 'Error $this->tabla esta vacio',data:  $this->tabla);
        }

        if($registro_id<=0){
            return $this->errores->error(mensaje: 'Error $registro_id debe ser mayor a 0',data:  $registro_id);
        }
        return true;
    }

