    /**
     * Integra los parametros de un key para select
     * @param int $cols N cols css
     * @param bool $con_registros integra rows en opciones si es true
     * @param array $filtro Filtro para result
     * @param string $key Name input
     * @param array $keys_selects keys precargados
     * @param int|null $id_selected Identificador para selected
     * @param string $label Etiqueta a mostrar
     * @param array $columns_ds Columnas para mostrar en select
     * @param bool $disabled Atributo disabled
     * @return array
     * @version 0.288.38
     */
    final protected function key_select(int $cols, bool $con_registros, array $filtro,string $key, array $keys_selects,
                                  int|null $id_selected, string $label, array $columns_ds = array(),
                                        bool $disabled = false): array
    {
        $key = trim($key);
        if($key === ''){
            return $this->errores->error(mensaje: 'Error key esta vacio',data:  $key);
        }
        $valida = (new validacion())->valida_cols_css(cols: $cols);
        if(errores::$error){
            return $this->errores->error(mensaje: 'Error al validar cols',data:  $valida);
        }

        $label = $this->label_init(key: $key,label:  $label);
        if(errores::$error){
            return $this->errores->error(mensaje: 'Error al generar label',data:  $label);
        }

        if(!isset($keys_selects[$key])){
            $keys_selects[$key] = new stdClass();
        }

        $keys_params = array('cols','con_registros','label','id_selected','filtro','columns_ds','disabled');

        foreach ($keys_params as $key_val){

            /**
             * REFACTORIZAR
             */
            if($key_val === 'id_selected'){
                if(is_null($id_selected)){
                    $id_selected = -1;
                }
            }
            if(!isset($$key_val)){
                return $this->errores->error(mensaje: 'Error key val no es una variable valida',data:  $key_val);
            }

            $value = $$key_val;
            if(isset($keys_selects[$key]->$key_val)){
                $value=$keys_selects[$key]->$key_val;
            }

            $keys_selects = $this->integra_key_to_select(key: $key,key_val:  $key_val,keys_selects:  $keys_selects,
                value:  $value);
            if(errores::$error){
                return $this->errores->error(mensaje: 'Error al integrar keys',data:  $keys_selects);
            }
        }



        return $keys_selects;
    }

